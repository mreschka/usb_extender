/****************************************************************************
  Function:
    bool USB_ApplicationEventHandler( uint8_t address, USB_EVENT event,
                void *data, uint32_t size )

  Summary:
    This is the application event handler.  It is called when the stack has
    an event that needs to be handled by the application layer rather than
    by the client driver.

  Description:
    This is the application event handler.  It is called when the stack has
    an event that needs to be handled by the application layer rather than
    by the client driver.  If the application is able to handle the event, it
    returns true.  Otherwise, it returns false.

  Precondition:
    None

  Parameters:
    uint8_t address    - Address of device where event occurred
    USB_EVENT event - Identifies the event that occured
    void *data      - Pointer to event-specific data
    uint32_t size      - Size of the event-specific data

  Return Values:
    true    - The event was handled
    false   - The event was not handled

  Remarks:
    The application may also implement an event handling routine if it
    requires knowledge of events.  To do so, it must implement a routine that
    matches this function signature and define the USB_HOST_APP_EVENT_HANDLER
    macro as the name of that function.
  ***************************************************************************/

void* keyboard;

bool USB_HOST_APP_EVENT_HANDLER ( uint8_t address, USB_EVENT event, void *data, uint32_t size )
{
    switch( (int)event )
    {
        /* Standard USB host events ******************************************/
        case EVENT_VBUS_REQUEST_POWER:
        case EVENT_VBUS_RELEASE_POWER:
        case EVENT_HUB_ATTACH:
        case EVENT_UNSUPPORTED_DEVICE:
        case EVENT_CANNOT_ENUMERATE:
        case EVENT_CLIENT_INIT_ERROR:
        case EVENT_OUT_OF_MEMORY:
        case EVENT_UNSPECIFIED_ERROR:
            return true;
            break;

        case EVENT_OVERRIDE_CLIENT_DRIVER_SELECTION:
        case EVENT_HOLD_BEFORE_CONFIGURATION:
            return false;
            break;
            
        /* GENERIC Class Specific Events ******************************************/
        case EVENT_GENERIC_ATTACH:
            return APP_HostGenericAttachEvent(address, data);
            break;
        case EVENT_GENERIC_DETACH:
            return APP_HostGenericDetachEvent(address);
            break;
        case EVENT_GENERIC_TX_DONE:
            return APP_HostGenericTxDoneEvent(address, *((uint32_t*)data));
            break;
        case EVENT_GENERIC_RX_DONE:
            return APP_HostGenericRxDoneEvent(address, *((uint32_t*)data));
            break;

        default:
            break;
    }

    return false;

}

